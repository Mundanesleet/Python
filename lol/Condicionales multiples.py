print("===================================")
print("¡¡Convertidor de numeros a letras!!")
print("===================================")

num_uno = int(input("¿Cual es el numero que deseas convertir?: "))

if num_uno == 1:
    print("El numero es 'Uno'")
elif num_uno == 2:
    print("El numero es 'Dos'")
elif num_uno == 3:
    print("El numero es 'Tres'")
elif num_uno == 4:
    print("El numero es 'Cuatro'")
elif num_uno == 5:
    print("El numero es 'Cinco'")
elif num_uno == 6:
    print("El numero es 'Seis'")
elif num_uno == 7:
    print("El numero es 'Siete'")
elif num_uno == 8:
    print("El numero es 'Ocho'")
elif num_uno == 9:
    print("El numero es 'Nueve'")
elif num_uno == 10:
    print("El numero es 'Diez'")
else:
    print("Este programa solo puede convertir hasta el numero 10")
print("Fin.")
